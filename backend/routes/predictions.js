// Get all predictions for a crop
router.get('/all/:crop', (req, res) => {
  try {
    const { crop } = req.params;
    const stmt = db.prepare(`
      SELECT * FROM predictions 
      WHERE crop = ? 
      ORDER BY created_at DESC 
      LIMIT 100
    `);
    
    const predictions = stmt.all(crop);
    res.json(predictions);
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
});

// Get prediction accuracy stats
router.get('/accuracy/:crop/:market', (req, res) => {
  try {
    const { crop, market } = req.params;
    
    // Compare predictions with actual prices
    const stmt = db.prepare(`
      SELECT 
        p.prediction_date,
        p.predicted_price,
        h.price as actual_price,
        ABS(p.predicted_price - h.price) / h.price * 100 as error_percent
      FROM predictions p
      JOIN historical_prices h ON p.crop = h.crop AND p.market = h.market
      WHERE p.crop = ? AND p.market = ?
      AND h.arrival_date = p.prediction_date
      ORDER BY p.prediction_date DESC
      LIMIT 30
    `);
    
    const comparisons = stmt.all(crop, market);
    
    if (comparisons.length === 0) {
      return res.json({ message: 'No accuracy data yet. Predictions need time to validate.' });
    }
    
    const avgError = comparisons.reduce((sum, c) => sum + c.error_percent, 0) / comparisons.length;
    const accuracy = Math.max(0, 100 - avgError);
    
    res.json({
      accuracy: accuracy.toFixed(2),
      averageError: avgError.toFixed(2),
      totalComparisons: comparisons.length,
      recentComparisons: comparisons.slice(0, 10)
    });
  } catch (error) {
    res.status(500).json({ error: error.message });
  }
});

export default router;
