      "Jowar": { market: "Mumbai", state: "Maharashtra" },
      "Maize": { market: "Hyderabad", state: "Telangana" },
      "Mango": { market: "Hyderabad", state: "Telangana" },
      "Mustard": { market: "Jaipur", state: "Rajasthan" },
      "Onion": { market: "Mumbai", state: "Maharashtra" },
